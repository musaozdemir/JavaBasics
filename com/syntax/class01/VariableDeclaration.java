package com.syntax.class01;

public class VariableDeclaration {

	public static void main(String[] args) {

		// one line declaration

		int a = 11;

		// two line declaration

		float b = 3.14F;

		// multi line declaration

		byte x, y, z;
		x = 10;
		y = 20;
		z = 30;

		System.out.println("toplam sayimiz=" + (b + x + y + z + a));

	}

}
