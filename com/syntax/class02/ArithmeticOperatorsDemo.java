		int num2 = 5;
		
		int sum = num1 + num2;
		
		int sub = num1 - num2;
		
		int div = num1 / num2;
		
		int mult = num1 * num2;
	
		
		System.out.println("addition -> " + sum);
		System.out.println("subtraction -> " + sub);
		System.out.println("division -> " + div);
		System.out.println("multiplication -> " + mult);
		
		double d1 = 3.14;
		double d2 = 6.36;
		
		double dSum = d1+d2;
		
		System.out.println("double addtion " + dSum);
		
